Lrmsd<-function(lrmsd,lrmsd2){

a=read.table(lrmsd)$V2
b=read.table(lrmsd2)$V2

plot(a[1:50],type="l",ylim=c(0,max(a[1:50],b[1:50])), main=lrmsd)
lines(b[1:50],col='blue')
abline(h=1,lty = 3)
abline(h=2,lty = 4)

print('LIGAND 1')
print(' ')
print('RMSD of best scored')
print(a[1])
print('min RMSD')
print(min(a))
print('with rank')
print(which(a==min(a)))
print('Nb of rmsd < 2A in 10 first')
print(length(which(a[1:10]<=2)))
print('Nb of rmsd < 2A in 50 first')
print(length(which(a[1:50]<=2)))
print('Nb of rmsd < 5A in 10 first')
print(length(which(a[1:10]<=5)))
print('Nb of rmsd < 5A in 50 first')
print(length(which(a[1:50]<=5)))
a50=min(a[1:50])
a200=min(a[1:200])
print('best rmsd in 1:50')
print(a50)
print('with rank')
print(which(a[1:50]==a50))
if (a50>a200){
	print('best rmsd in 1:200')
	print(a200)
	print('with rank')
	print(which(a[1:200]==a200))
	}

print('***************************************')
print('LIGAND 2')
print(' ')
print('RMSD of best scored')
print(b[1])
print('min RMSD')
print(min(b))
print('with rank')
print(which(b==min(b)))
print('Nb of rmsd < 2A in 10 first')
print(length(which(b[1:10]<=2)))
print('Nb of rmsd < 2A in 50 first')
print(length(which(b[1:50]<=2)))
print('Nb of rmsd < 5A in 10 first')
print(length(which(b[1:10]<=5)))
print('Nb of rmsd < 5A in 50 first')
print(length(which(b[1:50]<=5)))
b50=min(b[1:50])
b200=min(b[1:200])
print('best rmsd in 1:50')
print(b50)
print('with rank')
print(which(b[1:50]==b50))
if (b50>b200){
	print('best rmsd in 1:200')
	print(b200)
	print('with rank')
	print(which(b[1:200]==b200))
	}
dev.copy2eps(file='RMSD.eps',height=5,width=15)
}
